/**
 * Container class to define a "fingerprint"
 * @author Nehemiah Cionelo
 */
public class Fingerprint {
    private final String fingerprint;
    public Fingerprint(String print) {
        fingerprint = print;
    }
    /**
     * @return the fingerprint
     */
    public String getFingerprint() {
        return fingerprint;
    }
}
